#REST stands for Representational State Tranfer

#It is an software architectural style that is often
#used in the development of Web Services. It uses simple HTTP
#to make calls between machines rather than complex mechanisms

#REST is often preferred to approaches such as COBRA or SOAP
#for the following reasons:

#Consumes less bandwidth
#Stateless
#Easier to handle by the receiver

#Sinatra works well with REST because it supports:

#All HTTP verbs(e.g. GET, POST, PUT, DELETE) - these are methods
#Caching
#Content types
#Routes (the verb method takes the route as a parameter)

